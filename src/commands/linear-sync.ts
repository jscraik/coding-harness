/**
 * CLI wrapper for linear-sync.
 *
 * @param options - CLI-level options
 * @returns Process exit code
 */
export async function runLinearSyncCLI(
	options: LinearSyncOptions,
): Promise<number> {
	const result = await runLinearSync(options);

	if (!result.ok) {
		if (options.json) {
			console.error(
				JSON.stringify({ ok: false, error: result.error }, null, 2),
			);
		} else {
			console.error(`Error: ${result.error.message}`);
		}
		switch (result.error.code) {
			case "VALIDATION_ERROR":
				return 1;
			case "NOT_FOUND":
				return 2;
			case "PERMISSION_DENIED":
				return 3;
			default:
				return 10;
		}
	}

	const { results, dryRun } = result;
	const created = results.filter((r) => r.action === "created").length;
	const updated = results.filter((r) => r.action === "updated").length;
	const skipped = results.filter((r) => r.action === "skipped").length;

	if (options.json) {
		console.info(
			JSON.stringify(
				{
					ok: true,
					dryRun,
					summary: { total: results.length, created, updated, skipped },
					results,
				},
				null,
				2,
			),
		);
	} else {
		const prefix = dryRun ? "[dry-run] " : "";
		console.info(
			`${prefix}Synced ${results.length} finding(s): ${created} created, ${updated} updated, ${skipped} skipped`,
		);
		for (const r of results) {
			const issueRef = r.issue ? ` → ${r.issue.identifier} ${r.issue.url}` : "";
			const skipNote = r.skippedReason ? ` (${r.skippedReason})` : "";
			console.info(`  [${r.action}]${skipNote} ${r.finding.title}${issueRef}`);
		}
	}

	return 0;
}
