/**
 * Extract the version from a biome $schema URL.
 * e.g. "https://biomejs.dev/schemas/1.9.4/schema.json" → "1.9.4"
 */
function extractBiomeSchemaVersion(schemaUrl: string): string | undefined {
	const match = schemaUrl.match(/\/schemas\/([0-9]+\.[0-9]+\.[0-9]+)\//);
	return match?.[1];
}

/**
 * Read the $schema version from an existing biome.json.
 * Returns undefined if the file can't be parsed or has no $schema.
 */
function readExistingBiomeVersion(targetPath: string): string | undefined {
	try {
		const content = readFileSync(targetPath, "utf-8");
		const parsed = JSON.parse(content) as unknown;
		if (!parsed || typeof parsed !== "object") return undefined;
		const schema = (parsed as Record<string, unknown>).$schema;
		if (typeof schema !== "string") return undefined;
		return extractBiomeSchemaVersion(schema);
	} catch {
		return undefined;
	}
}

/**
 * JSC-57: Version-aware decision for tooling config files during init.
 *
 * Returns:
 *  - "skip"         — existing version is newer or equal to template; don't overwrite
 *  - "force_update" — existing version is older than template; upgrade it
 *  - "no_opinion"   — not a versioned tooling file, or file doesn't exist
 */
export function getToolingVersionDecision(
	templatePath: string,
	targetPath: string,
): "skip" | "force_update" | "no_opinion" {
	if (templatePath !== "biome.json") return "no_opinion";
	if (!existsSync(targetPath)) return "no_opinion";

	const existingVersion = readExistingBiomeVersion(targetPath);
	if (!existingVersion) return "no_opinion";

	// Semver comparison without external deps
	const templateParts = BIOME_SCHEMA_VERSION.split(".").map(Number);
	const existingParts = existingVersion.split(".").map(Number);

	for (let i = 0; i < 3; i++) {
		const t = templateParts[i] ?? 0;
		const e = existingParts[i] ?? 0;
		if (e > t) return "skip"; // existing is newer — don't downgrade
		if (e < t) return "force_update"; // existing is older — upgrade
	}
	return "skip"; // versions equal — already up to date
}

/**
 * Preserve the legacy boolean API used by interactive init flows.
 *
 * @returns `true` when the target tooling config is newer than the scaffold
 * template and should not be downgraded.
 */
export function shouldSkipDueToNewerToolingVersion(
	templatePath: string,
	targetPath: string,
): boolean {
	const decision = getToolingVersionDecision(templatePath, targetPath);
	return decision === "skip";
}

/**
 * Render the Codex local-environment TOML for the repository.
 *
 * Produces a TOML document (prefixed with the autogen header) that contains a setup script and a set of action blocks:
 * standard Tools/Run/Debug/Test actions, required Codex tool actions, and one action per script discovered in `context.packageScripts`.
 *
 * @param packageManager - Package manager identifier used to render install and script invocation commands (e.g., "npm", "yarn", "pnpm")
 * @param context - Template render context containing `packageScripts` and repository metadata used to build the action blocks
 * @returns The complete TOML content for the Codex local environment file, including the autogen header
 */
function renderCodexEnvironmentTemplate(
	packageManager: string,
	context: TemplateRenderContext,
): string {
	const installCommand = `if command -v mise >/dev/null 2>&1; then
  mise trust --yes .mise.toml || true
  mise install
fi
if [[ -f scripts/prepare-worktree.sh ]]; then
  bash scripts/prepare-worktree.sh
else
  ${renderInstallCommand(packageManager)}
fi`;
	const runScript = pickScriptForIcon(context.packageScripts, "run");
	const debugScript = pickScriptForIcon(context.packageScripts, "debug");
	const testScript = pickScriptForIcon(context.packageScripts, "test");

	const actions: CodexAction[] = [
		{
			name: "Tools",
			icon: "tool",
			command: `set -euo pipefail

${installCommand}`,
		},
		{
			name: "Run",
			icon: "run",
			command: runScript
				? renderScriptActionCommand(packageManager, runScript)
				: renderMissingScriptActionCommand("run"),
		},
		{
			name: "Debug",
			icon: "debug",
			command: debugScript
				? renderScriptActionCommand(packageManager, debugScript)
				: renderMissingScriptActionCommand("debug"),
		},
		{
			name: "Test",
			icon: "test",
			command: testScript
				? renderScriptActionCommand(packageManager, testScript)
				: renderMissingScriptActionCommand("test"),
		},
	];

	for (const action of REQUIRED_CODEX_TOOL_ACTIONS) {
		actions.push({
			name: action.name,
			icon: action.icon,
			command: `set -euo pipefail

${action.command}`,
		});
	}

	for (const script of context.packageScripts) {
		actions.push({
			name: `Script: ${script}`,
			icon: inferScriptIcon(script),
			command: renderScriptActionCommand(packageManager, script),
		});
	}

	const actionBlocks = actions
		.map((action) => renderCodexActionBlock(action))
		.join("\n\n");

	return `${CODEX_ENVIRONMENT_AUTOGENERATED_HEADER}
version = 1
name = "harness local environment"

[setup]
script = '''
set -euo pipefail

${installCommand}
'''

${actionBlocks}
`;
}

/**
 * Build a GitHub issue template configuration YAML that provides curated contact links.
 *
 * The generated YAML disables blank issues and constructs `contact_links` entries:
 * - Adds a Linear intake link unless `context.issueTracker` is `"github"` or `"none"`.
 * - Adds a "Repository docs" link when `context.repoUrl` is present (points at the repo README).
 * - Always adds a private security disclosure link using `context.securityEmail` (falls back to `security@example.com`).
 *
 * @param context - Template render context; used fields: `issueTracker`, `issueTrackingUrl`, `repoUrl`, and `securityEmail`.
 * @returns A YAML string for `.github/ISSUE_TEMPLATE/config.yml` with `blank_issues_enabled: false` and a `contact_links` list.
 */
function renderIssueTemplateConfig(context: TemplateRenderContext): string {
	const securityEmail = context.securityEmail ?? "security@example.com";
	const repoDocsUrl = context.repoUrl
		? `${context.repoUrl.replace(/\.git$/, "")}#readme`
		: undefined;
	const contactLinks = [];

	if (context.issueTracker !== "github" && context.issueTracker !== "none") {
		const linearUrl = context.issueTrackingUrl ?? "https://linear.app/";
		contactLinks.push(
			`  - name: Linear work intake
    url: ${linearUrl}
    about: Create or update bugs, features, policy gaps, automation work, and release follow-ups in Linear.`,
		);
	}

	if (repoDocsUrl) {
		contactLinks.push(
			`  - name: Repository docs
    url: ${repoDocsUrl}
    about: Review setup, workflow, and command documentation before opening new work.`,
		);
	}
	contactLinks.push(
		`  - name: Private security disclosure
    url: mailto:${securityEmail}
    about: Report security vulnerabilities privately instead of using public issue flows.`,
	);
	return `# Issue template configuration
blank_issues_enabled: false
contact_links:
${contactLinks.join("\n")}
`;
}

