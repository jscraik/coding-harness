function isAutogeneratedCodexEnvironment(path: string): boolean {
	if (!existsSync(path)) {
		return false;
	}

	try {
		const firstLine = readFileSync(path, "utf-8").split("\n", 1)[0] ?? "";
		return firstLine.trim() === CODEX_ENVIRONMENT_AUTOGENERATED_HEADER;
	} catch {
		return false;
	}
}

