/**
 * Read and parse a package.json file from a directory.
 *
 * @param targetDir - Directory to read `package.json` from
 * @returns The parsed `package.json` as `PackageJsonLike` if the file exists and contains a valid JSON object, `undefined` otherwise
 */
function readPackageJson(targetDir: string): PackageJsonLike | undefined {
	const packageJsonPath = resolve(targetDir, "package.json");
	if (!existsSync(packageJsonPath)) {
		return undefined;
	}

	try {
		const parsed = JSON.parse(
			readFileSync(packageJsonPath, "utf-8"),
		) as unknown;
		if (!parsed || typeof parsed !== "object") {
			return undefined;
		}
		return parsed as PackageJsonLike;
	} catch {
		return undefined;
	}
}

function readPackageScripts(targetDir: string): string[] {
	const parsed = readPackageJson(targetDir);
	const { scripts } = parsed ?? {};
	if (!scripts || typeof scripts !== "object") {
		return [];
	}
	return Object.entries(scripts)
		.filter(
			([name, value]) => typeof name === "string" && typeof value === "string",
		)
		.map(([name]) => name);
}

function readIssueTrackingUrl(targetDir: string): string | undefined {
	const parsed = readPackageJson(targetDir);
	if (!parsed) {
		return undefined;
	}
	const { bugs } = parsed;
	if (typeof bugs === "string") {
		return bugs.trim() || undefined;
	}
	if (bugs && typeof bugs === "object" && typeof bugs.url === "string") {
		const trimmed = bugs.url.trim();
		return trimmed || undefined;
	}
	return undefined;
}

function readProjectName(targetDir: string): string | undefined {
	const parsed = readPackageJson(targetDir);
	if (!parsed?.name || typeof parsed.name !== "string") {
		return undefined;
	}
	// Strip scope prefix if present (e.g., @brainwav/coding-harness -> coding-harness)
	const name = parsed.name.replace(/^@[^/]+\//, "").trim();
	return name || undefined;
}

function readRepoUrl(targetDir: string): string | undefined {
	const parsed = readPackageJson(targetDir);
	if (!parsed) {
		return undefined;
	}
	const { repository } = parsed;
	if (typeof repository === "string") {
		return normalizeRepoUrl(repository);
	}
	if (
		repository &&
		typeof repository === "object" &&
		typeof repository.url === "string"
	) {
		return normalizeRepoUrl(repository.url);
	}
	return undefined;
}

function normalizeRepoUrl(repositoryUrl: string): string | undefined {
	const trimmed = repositoryUrl.trim();
	if (!trimmed) {
		return undefined;
	}

	const withoutGitPrefix = trimmed.replace(/^git\+/, "");
	const withoutFragment = withoutGitPrefix.replace(/#[^#]+$/, "");
	const sshStyleMatch = withoutFragment.match(
		/^(?:ssh:\/\/)?git@([^/:]+)[:/]([^#]+)$/i,
	);
	if (sshStyleMatch) {
		const host = sshStyleMatch[1];
		const repoPath = sshStyleMatch[2]?.replace(/\.git$/, "");
		if (host && repoPath) {
			return `https://${host}/${repoPath}.git`;
		}
	}

	const shorthandPrefixMatch = withoutFragment.match(
		/^(github|gitlab|bitbucket):([^/][^#]+)$/i,
	);
	if (shorthandPrefixMatch) {
		const provider = shorthandPrefixMatch[1]?.toLowerCase();
		const repoPath = shorthandPrefixMatch[2]?.replace(/\.git$/, "");
		const host =
			provider === "github"
				? "github.com"
				: provider === "gitlab"
					? "gitlab.com"
					: "bitbucket.org";
		return `https://${host}/${repoPath}.git`;
	}

	const bareGithubShorthandMatch = withoutFragment.match(
		/^[A-Za-z0-9_.-]+\/[A-Za-z0-9_.-]+$/,
	);
	if (bareGithubShorthandMatch) {
		return `https://github.com/${withoutFragment.replace(/\.git$/, "")}.git`;
	}

	return withoutFragment || undefined;
}

function extractLinearProjectSlug(
	issueTrackingUrl: string | undefined,
): string | undefined {
	if (!issueTrackingUrl) {
		return undefined;
	}
	// Match patterns like https://linear.app/jscraik/project/coding-harness-bb735dbbda79
	const match = issueTrackingUrl.match(
		/linear\.app\/[^/]+\/project\/([a-z0-9-]+)/i,
	);
	return match?.[1] ?? undefined;
}

/**
 * Detect package manager from lock files.
 * Returns the package manager based on which lock file exists.
 */
export function detectPackageManager(dir: string): "pnpm" | "yarn" | "npm" {
	if (existsSync(resolve(dir, "pnpm-lock.yaml"))) return "pnpm";
	if (existsSync(resolve(dir, "yarn.lock"))) return "yarn";
	if (existsSync(resolve(dir, "package-lock.json"))) return "npm";
	return "npm";
}

/**
 * Normalize and validate a CI provider identifier.
 *
 * @param value - The provider identifier to normalize; may be `undefined` or an empty string to use the default.
 * @returns `ok: true` with `value` set to the normalized provider (`github-actions` or `circleci`), or `ok: false` with an `INVALID_PATH` error describing unsupported values.
 */
export function normalizeCIProvider(
	value: string | undefined,
): { ok: true; value: CIProvider } | { ok: false; error: InitErrorOutput } {
	if (!value || value.trim().length === 0) {
		return { ok: true, value: DEFAULT_CI_PROVIDER };
	}

	if (value === "github-actions" || value === "circleci") {
		return { ok: true, value };
	}

	return {
		ok: false,
		error: {
			code: "INVALID_PATH",
			message: `Unsupported CI provider: ${value}. Expected one of: github-actions, circleci.`,
		},
	};
}

/**
 * Construct a TemplateRenderContext populated from repository metadata and selected init options.
 *
 * The returned context includes discovered values (package scripts, project name, repo URL, issue tracker URL)
 * when available, a `securityEmail` resolved from the `HARNESS_SECURITY_EMAIL` environment variable or
 * `"security@example.com"` as a fallback, and any explicitly provided `ciProvider` or `projectType`.
 * If `options.issueTracker` is `"none"` or `"github"`, `linearProjectSlug` will be omitted.
 *
 * @param targetDir - Filesystem path of the repository to inspect for package and repository metadata
 * @param ciProvider - Optional CI provider to include in the context
 * @param projectType - Optional project type hint to include in the context
 * @param options - Init options that can toggle returned flags (e.g., setting `minimal` or `issueTracker`)
 * @returns A TemplateRenderContext containing discovered metadata and applied init options; fields that
 * are not discoverable or explicitly suppressed are omitted.
 */
export function createTemplateRenderContext(
	targetDir: string,
	ciProvider?: CIProvider,
	projectType?: ProjectType,
	options?: InitOptions,
): TemplateRenderContext {
	const issueTrackingUrl = readIssueTrackingUrl(targetDir);
	const projectName = readProjectName(targetDir);
	const repoUrl = readRepoUrl(targetDir);
	let linearProjectSlug = extractLinearProjectSlug(issueTrackingUrl);

	// Solo orchestration overrides
	if (options?.issueTracker === "none" || options?.issueTracker === "github") {
		// Suppress linear project slug if an alternative tracker or 'none' is explicitly requested
		linearProjectSlug = undefined;
	}

	// Resolve security contact email: env var override, then safe fallback value
	const securityEmail =
		process.env.HARNESS_SECURITY_EMAIL ?? "security@example.com";
	return {
		targetDir,
		...(ciProvider ? { ciProvider } : {}),
		packageScripts: readPackageScripts(targetDir),
		...(issueTrackingUrl ? { issueTrackingUrl } : {}),
		...(projectName ? { projectName } : {}),
		...(repoUrl ? { repoUrl } : {}),
		...(linearProjectSlug ? { linearProjectSlug } : {}),
		...(projectType ? { projectType } : {}),
		securityEmail,
		...(options?.minimal ? { minimal: true } : {}),
		...(options?.issueTracker ? { issueTracker: options.issueTracker } : {}),
	};
}

function scriptContainsToken(scriptName: string, token: string): boolean {
	const lower = scriptName.toLowerCase();
	return (
		lower === token ||
		lower.startsWith(`${token}:`) ||
		lower.endsWith(`:${token}`) ||
		lower.includes(`:${token}:`)
	);
}

function inferScriptIcon(scriptName: string): CodexActionIcon {
	const testTokens = ["test", "spec", "e2e", "integration", "unit", "coverage"];
	if (testTokens.some((token) => scriptContainsToken(scriptName, token))) {
		return "test";
	}

	const runTokens = ["dev", "start", "serve", "preview", "watch"];
	if (runTokens.some((token) => scriptContainsToken(scriptName, token))) {
		return "run";
	}

	const debugTokens = [
		"debug",
		"check",
		"lint",
		"typecheck",
		"verify",
		"validate",
		"doctor",
		"preflight",
	];
	if (debugTokens.some((token) => scriptContainsToken(scriptName, token))) {
		return "debug";
	}

	return "tool";
}

